    public void penaltyBook(String name) {
        Date date = new Date();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        date = jDateChooser1.getDate();
        String fecha = dateFormat.format(date);
        try {
            LoanFile loanFile = new LoanFile();
            List<Loan> loanList = new ArrayList<Loan>();
            loanList = loanFile.returnLoanArray();
            for (int i = 0; i < loanList.size(); i++) {
                if (loanList.get(i).getBook() != null) {
                    if (loanList.get(i).getBook().getName().equals(name)) {
                        if (date.after(loanList.get(i).getEndDate())) {
                            JOptionPane.showMessageDialog(null, "You have a $25 fine for returning the book after the indicated date");
                        } else {
                            JOptionPane.showMessageDialog(null, "You have returned the book on time");
                        }
                    }
                }
            }
        } catch (IOException ex) {
        } catch (ClassNotFoundException ex) {
        }
    }

