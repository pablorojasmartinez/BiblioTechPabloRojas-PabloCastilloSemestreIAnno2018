    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LoanFile loanFile = new LoanFile();
        try {
            BookFile bookFile = new BookFile();
            AudiovisualFile audiovisual = new AudiovisualFile();
            if (this.loan.getBook() != null) {
                penaltyBook(this.loan.getBook().getName());
                bookFile.returnBook(this.loan.getBook().getCode());
                loanFile.deleteBook(this.loan.getBook().getCode());
            } else if (this.loan.getAudio() != null) {
                penaltyAudiovisual(this.loan.getAudio().getBrand());
                audiovisual.returnAudiovisual((this.loan.getAudio().getBrand()));
                loanFile.deleteAudiovisual(this.loan.getAudio().getBrand());

            }
            this.dispose();
        } catch (IOException ex) {
            Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


